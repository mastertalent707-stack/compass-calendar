// Tailwind xl breakpoint; the sidebar auto-collapses below this width.
export const SIDEBAR_AUTO_COLLAPSE_BREAKPOINT = 1280;

// Below this width the day view can no longer fit the task list at its
// minimum width next to a usable calendar (task list 240 + divider 32 +
// calendar 320 + main-section padding + hour labels), so it auto-collapses.
export const TASK_LIST_AUTO_COLLAPSE_BREAKPOINT = 720;
